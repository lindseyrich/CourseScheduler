    private void dropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentButtonActionPerformed
        String name = studentToDropComboBox.getSelectedItem().toString();
        String[] fullName = name.split(" ");
        dropStudentTextArea.append(fullName[0] +" " +  fullName[1] +  " has been dropped from the list of students.\n");
        String studentToDropID = "";
  
        if (StudentQueries.getAllStudents().size() != 0){
            for(int i = 0; i < StudentQueries.getAllStudents().size(); i++){
                StudentEntry student = StudentQueries.getAllStudents().get(i);
                if (student.getFirstName().contains(fullName[0]) && student.getLastName().contains(fullName[1])){
                    studentToDropID = student.getStudentID();

                    break;

                }

            }
        }
        
        
        
        if(SemesterQueries.getSemesterList().size() != 0){
            for(int j = 0; j < SemesterQueries.getSemesterList().size(); j++){
                String semester = SemesterQueries.getSemesterList().get(j);
                dropStudentTextArea.append("For semester: " + semester + "\n");
                if(ScheduleQueries.getCoursesByStudent(studentToDropID).size() != 0){
                    for(int i = 0; i < ScheduleQueries.getCoursesByStudent(studentToDropID).size(); i++){
                
                        String course = ScheduleQueries.getCoursesByStudent(studentToDropID).get(i);
                        if (ScheduleQueries.getWaitlistedStudentsByCourse(semester, course).size() != 0){
                            
                            ScheduleEntry Entry = ScheduleQueries.getWaitlistedStudentsByCourse(semester, course).get(0);
                            
                            ScheduleQueries.updateScheduleEntry(semester, Entry);
                            StudentEntry student = StudentQueries.getStudent(Entry.getStudentID());
                            if(student.getStudentID() != studentToDropID){
                                String firstName = student.getFirstName();
                                String lastName = student.getLastName();
                                String ID = student.getStudentID();
                                dropStudentTextArea.append(fullName[0] + " " + fullName[1] + " "+ studentToDropID + " has been dropped from " + course + "\n");
                                dropStudentTextArea.append(firstName + " " + lastName + " " + ID +  " will be notified that they are scheduled in "+ course + "\n");
                            }
                            else{
                                dropStudentTextArea.append(fullName[0] + " " + fullName[1] + " "+ studentToDropID + " has been removed from the waiting list for  " + course + "\n");
                            
                            
                            
                            }
            
                        }
        
        
                    }
                }
            }
        
        }
        
        
        
        StudentQueries.dropStudent(studentToDropID);
        ScheduleQueries.deleteScheduleByStudent(currentSemester, studentToDropID);
        
        rebuildStudentComboBoxes();
        
        
    }//GEN-LAST:event_dropStudentButtonActionPerformed

