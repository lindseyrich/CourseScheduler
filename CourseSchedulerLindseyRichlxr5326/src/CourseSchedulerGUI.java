    private void selectCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectCourseButtonActionPerformed
        String courseSelected = courseOptionsComboBox.getSelectedItem().toString();
        currentCourse = courseSelected;

        String label = "";
        for (int i = 0; i < CourseQueries.getAllCourses(currentSemester).size(); i++){

            CourseEntry course = CourseQueries.getAllCourses(currentSemester).get(i);

            if (course.getCourseCode().contains(courseSelected)){

                int x = CourseQueries.getCourseSeats(currentSemester, courseSelected);
                int y = ScheduleQueries.getScheduledStudentCount(currentSemester, courseSelected);
                seats = x-y;

                label += (course.getCourseCode() + "-" + course.getDescription());

                break;
            }

        }

        courseSelectedLabel.setText(label);
    }//GEN-LAST:event_selectCourseButtonActionPerformed

